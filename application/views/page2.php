                                    <li><span style="color: #000000;">Hewan aqiqah harus dalam keadaan sehat, tidak boleh ada cacat dan dalam keadaan sakit.</span></li>
                                    <li><span style="color: #000000;">Hewan aqiqah harus merupakan hewan yang sudah layak disembelih seperti mana halnya&nbsp;&nbsp; kurban.&nbsp; Jika kambing, maka minimal sudah berusia satu tahun.</span></li>
                                    <li><span style="color: #000000;">Disunnahkan dimasak terlebih dahulu.</span></li>
                                </ol>
                                <p style="text-align: justify;"><br /><span style="color: #000000;"> "Sesungguhnya Rasulullah Shallallahu 'alaihi wa sallam memerintahkan mereka aqiqah untuk anak laki-laki dua kambing, dan anak perempuan satu kambing". [HR At Tirmidzi dan Ibnu Majah].</span><br /> <br /><span style="color: #000000;"> Ketentuan kambingnya disini tidak dijelaskan jenisnya, harus jantan atau boleh juga betina. Namun para ulama menyatakan, bahwa kambing aqiqah sama dengan kambing kurban dalam usia, jenis dan bebas dari aib dan cacat. Akan tetapi mereka tidak merinci tentang disyaratkan jantan atau betina. Oleh karena itu, kata "syah" dalam hadits di atas, menurut bahasa Arab dan istilah syari&rsquo;at mencakup kambing atau domba, baik jantan maupun betina. Tidak ada satu hadits atau atsar yang mensyaratkan jantan dalam hewan kurban. Pengertian "syah" dikembalikan kepada pengertian syariat dan bahasa Arab.</span><br /> <br /><span style="color: #000000;"> Dengan demikian, maka sah bila seseorang menyembelih kambing betina dalam kurban dan aqiqah, walaupun yang utama dan dicontohkan Rasulullah Shallallahu 'alaihi wa sallam ialah kambing jantan yang bertanduk. Wallahu a&rsquo;lam.</span></p>
                                <p style="text-align: justify;"><br /><span style="color: #000000;">Distribusi Masakan aqiqah :</span><br /><br /><span style="color: #000000;">Setelah disembelihnya hewan aqiqah, maka para ulama menganjurkan untuk membaginya menjadi tiga bagian. Sepertiga pertama untuk ahlul bait (kerabat dekat) sepertiga kedua untuk diberikan kepada orang lain sebagai hadiah, dan sepertiga terakhir untuk dijadikan sebagai sedekah.</span><br /><br /><span style="color: #000000;">Dianjurkan pula bahwa pemberian untuk sedekah dan hadiah, lebih utama jika dilakukan setelah daging tadi dimasak oleh ahlulbait, tidak dibagikan dalam keadaan masih mentah. Hal ini mengingat tidak semua fakir miskin dalam keadaan mampu untuk memasak daging yang diberikannya, dan kalaupun sanggup akan menambah beban mereka. Maka yang paling utama adalah meringankan beban mereka dan memberikan kebahagiaan dan kesenangan bagi mereka.</span><br /><br /><span style="color: #000000;">Jumlah Hewan aqiqah :</span><br /><br /><span style="color: #000000;">Jumlah hewan Aqiqah untuk anak laki-laki dua ekor sedangkan untuk anak perempuan satu ekor. Akan tetapi jika tidak mampu dua ekor untuk anak laki-laki maka seekorpun boleh. Hal ini InsyaAllah tidak akan mengurangi nilai Aqiqah, Asal kita jujur dan tidak berpura-pura tidak mampu. Sebab, sebagimana tampak dalam hadist yang bersumber dari Ibnu Abbas, Rasulullah pernah meng-Aqiqahi Hasan dan Huein masing-masing seekor kibasy.</span><br /><br /></p>              </div>
                        </div>

                    </div>
                    <div class="accordion-group">
                        <div class="accordion-heading">
                            <a class="accordion-toggle" data-toggle="collapse" href="#antara-aqiqah-dan-qurban">- Antara Aqiqah dan Qurban</a>
                        </div>

                        <div id="antara-aqiqah-dan-qurban" class="accordion-body collapse in">
                            <div class="accordion-inner">
                                <p style="text-align: justify;"><span style="color: #000000;">Jika pelaksanaan aqiqah bertepatan dengan bulan&ndash;bulan haji, apakah bisa digabungkan antara hewan qurban dengan aqiqah, dengan melaksanakan salah satunya saja. Ataukah antara aqiqah dan kurban itu sendiri merupakan hal yang sama?</span><br /><br /><span style="color: #000000;">Untuk permasalahan ini, para ulama kembali terbagi menjadi dua bagian ;</span></p>
                                <ol style="text-align: justify;">
                                    <li style="text-align: justify;"><span style="color: #000000;">Bahwa hewan qurban jika digabungkan dengan aqiqah, karena bertepatan dengan bulan haji, maka tidak menjadi masalah bagi ulama hambali, dan Muhammad bin Sirin serta Hasan Bashri. Diceritakan dalam satu riwayat bahwa ayah dari imam Ahmad, yaitu Hambal pernah membeli hewan qurban dan menyembelihnya di bulan haji dengan niat qurban sekaigus aqiqah.&nbsp; Dengan alasan inilah ulama di atas membolehkan kurban dan aqiqah dilaksanakan pada satu waktu dan satu niat, yaitu ketika idul adha.</span></li>
                                    <li style="text-align: justify;"><span style="color: #000000;">Yaitu pendapat ulama Maliki, yang berpendapat bahwa qurban dan aqiqah adalah hal yang berbeda. Dalam segi syariat keduanya sudah berbeda, sebab disyariatkan keduanya juga berbeda. Maka qurban dan aqiqah tidak bisa digabungkan satu sama yang lainnya.</span></li>
                                </ol>
                                <p style="text-align: justify;"><span style="color: #000000;">&nbsp;</span><br /><span style="color: #000000;">Aqiqah dan Qurban, mana yang lebih didahulukan?</span></p>
                                <p style="text-align: justify;"><br /><span style="color: #000000;">Mayoritas ulama berpendapat bahwa aqiqah maupun qurban hukumnya sunah muakkad (yang sangat ditekankan). Disebutkan dalam riwayat Muslim dari sahabat Ummu Salamah bahwa Nabi shallallahu &lsquo;alaihi wa sallam bersabda:</span></p>
                                <p style="text-align: justify;"><br /><span style="color: #000000;">&ldquo;Apabila kalian melihat hilal bulan dzulhijah dan kalian hendak berqurban maka jangan menyentuh rambut dan kukunya.&rdquo;</span><br /><br /><span style="color: #000000;">Kalimat: &lsquo;hendak berkurban&rsquo; menunjukkan bahwa qurban hukumnya sunah dan tidak wajib.</span><br /><span style="color: #000000;">Berdasarkan hal ini, yang terbaik adalah seseorang melaksanakan kedua sunah tersebut bersamaan. Karena keduanya dianjurkan untuk dilaksanakan. Jika tidak mampu melakukan keduanya dan waktu aqiqah berbeda di selain hari qurban, maka hendaknya mendahulukan yang lebih awal waktu pelaksanaannya. Akan tetapi jika akikahnya bertepatan dengan hari raya qurban, dan tidak mampu untuk menyembelih dua ekor kambing untuk akikah dan satunya untuk qurban, pendapat yang lebih kuat, sebaiknya mengambil pendapat ulama yang membolehkan menggabungkan aqiqah dan qurban.</span></p>              </div>
                        </div>

                    </div>
                    <div class="accordion-group">
                        <div class="accordion-heading">
                            <a class="accordion-toggle" data-toggle="collapse" href="#siapa-yang-bertanggung-jawab-dalam-aqiqah">- Siapa yang bertanggung jawab dalam aqiqah ?</a>
                        </div>

                        <div id="siapa-yang-bertanggung-jawab-dalam-aqiqah" class="accordion-body collapse in">
                            <div class="accordion-inner">
                                <p style="text-align: justify;"><span style="color: #ff6600;">SIAPA YANG BERTANGGUNG JAWAB DALAM AQIQAH</span><br />&nbsp;<br />Pertama :<br />Kalangan Hambali dan Maliki, berpendapat bahwa yang bertanggungjawab atas syariat aqiqah sesuai dengan khitab hadits yang telah disebutkan diatas, yaitu orang tua laki &ndash; laki, sang ayah. Dikuatkan kembali oleh pendapat imam Ahmad ketika ditanya mengenai seseorang yang belum diaqiqahkan oleh ayahnya bagaimana hukumnya, beliau menjawab : kewajiban itu atas ayahnya.<br />&nbsp;<!--more--><br />Kedua :<br />Jika si anak memiliki harta dan mampu melakukannya sendiri, maka dia yang bertanggung jawab atas dirinya sendiri. Akan tetapi jika tidak mampu dan masih memiliki ayah, maka ayahnya yang tanggungjawab. Sementara jika ia tidak mampu dan tidak lagi memiliki ayah, maka kewajibannya bagi sang ibu. Sebagaimana pendapat Ibnu Hazm adhzahiri.<br /><br />Ketiga :<br />Yang berhak mengaqiqahkan anak, adalah mereka yang bertanggungjawab dalam memberi nafkah atas kehidupan sehari &ndash; harinya ( wali ). Tidak mesti orang tua. Seperti yang dilakukan oleh Rasulullah saw, yang mengaqiqahkan cucu beliau Hasan dan Husein. Karena menurut beberapa pendapat bahwa Ali kala itu sedang dalam keadaan terhimpit. Ada yang mengatakan bahwa Ali sebelumhya memberikan hewan aqiqah kepada Rasul untuk kedua puteranya. Yang jelas, ini merupakan pendapat Imam Syafi&rsquo;i, bahwa kewajiban aqiqah atas anak, kembali kepada orang yang memelihara dan memberi nafkah padanya.<br /><br />Keempat :<br /><br />Yang bertanggungjawab atas aqiqah seorang anak, bukan ayah, bukan ibu dan bukan orang yang memberi nafkah hidupnya. Melainkan tidak ada orang yang tertentu yang diberikan kewajiban khusus untuk melaksanakan aqiqah. Sebagaimana di hadits &ndash; hadits yang telah disebutkan tidak ada &ldquo; qayid &ldquo; yang jelas bahwa kewajibannya khusus sang ayah, ibu, ataupun wali. Oleh karena itu sah &ndash; sah saja jika yang malaksanakannya orang lain selain mereka, seperti paman, sanak saudara atau bahkan orang asing sekalipun. Ini pendapat imam Ibnu Hajar dan Syaukani.<br /><br />Dari berbagai macam pendapat diatas, kita dapat menarik kesimpulan tidak ada pendapat yang sepakat ditentukan oleh ulama mengenai siapa yang bertanggungjawab dalam hal mengaqiqahkan sang anak. Maka menurut kami, yang berhak pertama kali adalah sang ayah, kemudian wali atau orang yang mengasuhnya, kemudian jika ada dari sanak saudaranya yang ingin mengaqiqahkannya maka itu juga diperbolehkan.</p>              </div>
                        </div>

                    </div>
                    <div class="accordion-group">
                        <div class="accordion-heading">
                            <a class="accordion-toggle" data-toggle="collapse" href="#aqiqah-bagi-orang-dewasa">- Aqiqah bagi orang Dewasa</a>
                        </div>

                        <div id="aqiqah-bagi-orang-dewasa" class="accordion-body collapse in">
                            <div class="accordion-inner">
                                <p style="text-align: justify;"><span style="color: #000000;">Belum pernah diaqiqah, padahal sudah baligh</span><br /><br /><span style="color: #000000;">Bagaimana hukumnya seseorang yang sudah besar tapi belum pernah diaqiqahkan oleh orang tuanya. Apakah dia masih harus aqiqah walau orang tuanya sudah meninggal ? Lalu bolehkan melaksanaan aqiqah sendiri?</span><br /><br /><span style="color: #000000;">Dalam permasalahan ini, ulama terbagi kepada dua pendapat :</span><br /><span style="color: #000000;">Pertama :</span><br /><span style="color: #000000;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Disunahkan bagi mereka yang belum sempat diaqiqahkan oleh orang tuanya, untuk melaksanakan aqiqah sendiri. Sebagaimana pendapat Atho&rsquo; , Hasan, Muhammad bin Sirin, dan sebagian kalangan Syafi&rsquo;i. Mereka menjadikan hadits yang menjelaskan bahwa nabi saw pernah melakukan aqiqah untuk dirinya sendiri sebagimana termaktub dalam kitab I'anathutholibin (Syarah dan kitab Fathul Mu'in Jus 2 Halaman 336) Bahawa Rasulullah Muhammad SAW melaksanakan Aqiqah untuk dirinnya sendiri sesudah beliau diangkat menjadi nabi (usia 40 tahun) </span><br /><span style="color: #000000;">&nbsp;</span><br /><span style="color: #000000;">Kedua :</span><br /><span style="color: #000000;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Tidak diwajibkan pada seorang anak yang belum sempat diaqiqahkan oleh orang tuanya untuk melakukan aqiqah sendiri. Karena aqiqah pada asalnya disyariatkan kepada orang tua atau wali yang memeliharanya. Maka tidak ada perintah untuk melakukannya sendiri. Pendapat ini yang dijadikan landasan kalangan Syafi&rsquo;i dan Ahmad bin Hambal.</span><br /><br /><span style="color: #000000;">Setelah jelas dua pendapat diatas, dan lemahnya dalil yang dijadikan landasan pendapat pertama. Terdapat beberapa keterangan dari para ulama terdahulu yang menjelaskan bahwa mereka melakukan aqiqah secara sendiri. Seperti keterangan yang didapatkan dari Imam Hasan al Bashri : &ldquo; jika belum sempat diaqiqahkan, maka lakukanlah aqiqah sendiri bagi anak laki &ndash; laki &ldquo;. Sebagaimana ungkapan Muhammad bin Sirin : &ldquo; aku melakukan aqiqahqu sendiri dengan seekor kambing &ldquo;.</span><br /><br /><span style="color: #000000;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Dari keterangan berikut dapat disimpulkan bahwa ulama tidak melarang untuk melakukannya secara sendiri. Maka bagi yang belum sempat diaqiqahkan oleh kedua orangtuanya, tidak mengapa jika ingin melakukannya sendiri. Sebagaimana tidak ada larangan untuk tidak melaksanakannya.</span><br /><span style="color: #000000;">&nbsp;</span><br /><br /></p>              </div>
                        </div>

                    </div>
                    <div class="accordion-group">
                        <div class="accordion-heading">
                            <a class="accordion-toggle" data-toggle="collapse" href="#hukum-aqiqah">- Hukum Aqiqah</a>
                        </div>

                        <div id="hukum-aqiqah" class="accordion-body collapse in">
                            <div class="accordion-inner">
                                <p><span style="color: #ff6600;"><strong>Hukum Aqiqah</strong><br /></span></p>
                                <p style="text-align: justify;">Sebagimana diungkapkan oleh Abdullah Nashih Ulwan dalam kitab Tarbiyatul Aulad fi Al-Islam, pendapat para <em>fuqoha</em> tentang hukum aqiqah terbagi menjadi tiga.</p>
                                <p style="text-align: justify;">Pertama&nbsp; adalah pendapat yang menyatakan bahwa aqiqah itu sunnah yang merupakan pendapat dari Imam Malik, Imam Syafi'i, Imam Ahmad dan Abu Tsaur.</p>
                                <p style="text-align: justify;">Kedua, pendapat yang menyatakan bahwa aqiqah itu adalah Wajib. ini merupakan pendapat dari Imam Hasan Al - Bashri, Al-Laits Ibnu Sa'ad dan yang lainnya. Dasar pendapat mereka adalah hadist yang diriwayatkan Muraidah dan Ishaq Bin Ruhawiah yang artinya : "Sesungguhnya manusia itu pada hari kiamat akan dimintakan pertanggungjawabannya atas Aqiqahnya seperti halnya pertanggungjawaban atas lima waktunnya"</p>
                                <p style="text-align: justify;">Ketiga, pendapat yang menolak disyariatkannya Aqiqah, Ini adalah pendapat ahli fiqih Hanafiah. Mereka berdasarkan pada hadist Abu Rafi, Bahwa Rasulullah pernah berkata kepada Fatimah, "Jangan engkau mengaqiqahinya tetapi cukurlah rambunya". Namun, dari mayoritas pada <em>fuqoha</em> berpendapat bahwa konteks hadist tersebut justru menguatkan disunnahkan dan dianjurkannya aqiqah, sebab Rasullulah sendiri telah mengaqiqahi Hasan dan Husein. Dengan demikian dapat disimpulkan bahwa mengaqiqahi anak itu sunnah dan diajurkan.</p>              </div>
                        </div>

